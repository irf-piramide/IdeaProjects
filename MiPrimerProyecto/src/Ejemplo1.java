public class Ejemplo1 {
    public static void main(String[] args) {
        System.out.println("Cat     " + "Gato");
        System.out.println("Dog     " + "Perro");
        System.out.println("Cloud   " + "Nube");
        System.out.println("Computer " + "Ordenador");
        System.out.println("----");
        System.out.println("Dog\t\t\tPerro");
        System.out.println("Cat\t\t\tGato");
        System.out.println("Cloud\t\tNube");
        System.out.println("Computer\tOrdenador");
    }
}
